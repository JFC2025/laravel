<?php
// Mostrar los números del 1 al 10 usando un bucle while.

// Declarar la variable del contador
$contador = 1;  
// Iniciar el bucle while
while ($contador <= 10) {
    // Mostrar el número actual
    echo "<html><head><title>Números del 1 al 10</title></head><body>";
    echo "<h3>Número: $contador</h3>";
    echo "</body></html>";
    
    // Incrementar el contador
    $contador++;
}


?>
